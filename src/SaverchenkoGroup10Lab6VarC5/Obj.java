package SaverchenkoGroup10Lab6VarC5;

public class Obj {

    private int x;
    private int y;

    private String name;

    public Obj (String name, int x, int y) {
        this.name=name;
        this.x=x;
        this.y = y;
    }

    public String getName() {
        return name;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
